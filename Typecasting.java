public class Typecasting {
    public static void main(String[] args) {
        double price = 100;
        double finalprice = price + 18;
        System.out.println(finalprice);

        // typecasting
        int Price = 100;
        int Finalprice = Price + (int) 18.0;
        System.out.println(Finalprice);
    }
}
